import java.util.*;

public class user {
    String firstName;
    String lastName;
    int age;
    String cardNo;
    String pin;
    Date exp;
    String ccv;

    public user(String firstName, String lastName, int age, String cardNo, String pin, Date exp, String ccv) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.cardNo = cardNo;
        this.pin = pin;
        this.exp = exp;
        this.ccv = ccv;
    }
}


